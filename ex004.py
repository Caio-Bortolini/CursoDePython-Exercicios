escreva = input('Digite algo: ')

print('È um numero?',escreva.isnumeric(), type(escreva))

print('È uma letra?', escreva.isalpha(), type(escreva))

print('Esta em letra maiuscula?', escreva.isupper(), type(escreva))

print('È letra minuscula?',escreva.islower(), type(escreva))

print('È decimal?', escreva.isdecimal(), type(escreva))




